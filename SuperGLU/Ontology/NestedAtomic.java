package Ontology;

public class NestedAtomic {

	public String indices;
	
	public NestedAtomic(String index)
	{
		this.indices=index;
	}
	
	public NestedAtomic()
	{
		this.indices="";
	}
	
	public String getIndices()
	{
		return indices;
	}
	
	public void setIndices(String index)
	{
		indices=index;
	}
	
}
